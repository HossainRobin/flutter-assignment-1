void main() {
  const a = 7;
  const b = 3;

  print("a = $a");
  print("b = $b");

  int add = a + b;
  print("Addition: $a + $b = $add");

  int sub = a - b;
  print("Subtraction: $a - $b = $sub");

  int multi = a * b;
  print("Multiplication: $a * $b = $multi");

  double division = a / b;
  print("Division: $a / $b = $division");

  dynamic modulus = a % b;
  print("Modulus: $a % $b = $modulus");
}
